'''
En Python tenemos 4 tipos de alcance
-Local scope alcance local
-Enclosing scope alcance encerrado
-Global scope alcance global
-Built-in scope alcance incorporado
'''

'''
Local scope
-Se refiere a una variable declarada dentro de una función, sólo podremos acceder al valor de la variable desde dentro de la función
'''

'''
Enclosing scope
-Se refiere a una función declarada dentro de otra función, o también conocido como función anidada,la función que está dentro podrá acceder a los valores de la función padre,
pero la función padre no podrá acceder a los valores de la función hija(anidada)
'''
'''
Global scope
-Se refiere a una variable declarada fuera de una función, cualquier función u otras variables pueden acceder a su valor
'''
'''
Built-in scope
-Se refiere a palabras reservadas de python, como print,for, in,def etc.
'''